package com.williamvanderhoef.foursquare.types;

public class Stats
{
	private Integer checkinsCount;
	private Integer usersCount;
	private Integer tipCount;
	
	
	/**
	 * @return the checkinsCount
	 */
	public Integer getCheckinsCount()
	{
		return checkinsCount;
	}
	/**
	 * @param checkinsCount the checkinsCount to set
	 */
	public void setCheckinsCount(Integer checkinsCount)
	{
		this.checkinsCount = checkinsCount;
	}
	/**
	 * @return the usersCount
	 */
	public Integer getUsersCount()
	{
		return usersCount;
	}
	/**
	 * @param usersCount the usersCount to set
	 */
	public void setUsersCount(Integer usersCount)
	{
		this.usersCount = usersCount;
	}
	
	public Integer getTipCount() {
		return tipCount;
	}
	public void setTipCount(Integer tipCount) {
		this.tipCount = tipCount;
	}
	
	

}
